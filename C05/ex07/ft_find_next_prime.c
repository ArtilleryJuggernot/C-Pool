int	ft_find_next_prime(int nb)
{
	int	i;

	if (nb < 3)
		return (2);
	if ((nb % 2) == 0)
		nb++;
	if (nb > 2147483629 && nb <= 2147483647)
		return (2147483647);
	i = 3;
	while (i * i <= nb)
	{
		if ((nb % i) == 0)
		{
			nb += 2;
			i = 1;
		}
		i += 2;
	}
	return (nb);
}
