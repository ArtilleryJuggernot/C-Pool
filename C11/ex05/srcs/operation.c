int	add(int x, int y)
{
	return (x + y);
}

int	minus(int x, int y)
{
	return (x - y);
}

int	div(int x, int y)
{
	return (x / y);
}

int	mul(int x, int y)
{
	return (x * y);
}

int	mod(int x, int y)
{
	return (x % y);
}
