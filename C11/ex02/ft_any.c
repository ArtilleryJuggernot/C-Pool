#include <stdlib.h>

int	ft_any(char **tab, int (*f)(char*))
{
	int	n;

	n = 0;
	while (tab[n])
	{
		if (f(tab[n]) != 0)
			return (1);
		n++;
	}
	tab = NULL;
	return (0);
}
